# ------------------------------------------------------------------
# Main simulation loop
# ------------------------------------------------------------------

def run_simulation():
    total = len(PATIENT_SCENARIOS)
    print(f"Starting simulation: {total} sessions, {MIN_TURNS}-{MAX_TURNS} turns each, session_id={SESSION_ID}\n")
    print("=" * 60)

    for i, scenario in enumerate(PATIENT_SCENARIOS, 1):
        patient_id = scenario["patient_id"]
        persona    = scenario["persona"]
        seed_msg   = scenario["seed_msg"]

        print(f"\n[{i}/{total}] {patient_id}")
        print(f"  {persona.splitlines()[0]}")
        print("-" * 50)

        reset_for_new_patient(patient_id)

        # history used by patient LLM: alternating patient/therapist turns
        patient_llm_history = []
        patient_msg = seed_msg

        for turn in range(1, MAX_TURNS + 1):
            # ---- Patient speaks ----
            print(f"  [{turn}] Patient  : {patient_msg[:90]}{'...' if len(patient_msg) > 90 else ''}")

            # ---- Therapist responds (full pipeline) ----
            try:
                therapist_msg = therapy_chat(patient_msg, patient_id=patient_id, session_id=SESSION_ID)
            except Exception as e:
                print(f"  [ERROR] therapy_chat failed at turn {turn}: {e}")
                break

            print(f"       Therapist: {therapist_msg[:90]}{'...' if len(therapist_msg) > 90 else ''}")

            # Update history for patient LLM
            patient_llm_history.append({"role": "patient",   "content": patient_msg})
            patient_llm_history.append({"role": "therapist", "content": therapist_msg})

            if turn == MAX_TURNS:
                print(f"  [MAX_TURNS reached]")
                break

            # ---- Patient LLM generates next reply ----
            try:
                raw = generate_patient_reply(persona, patient_llm_history)
            except Exception as e:
                print(f"  [ERROR] patient LLM failed at turn {turn}: {e}")
                break

            # Check for natural end signal (only valid after MIN_TURNS)
            wants_end = "[END]" in raw
            patient_msg = raw.replace("[END]", "").strip()

            if wants_end and turn >= MIN_TURNS:
                # Send the final closing message to the therapist, then stop
                print(f"  [{turn+1}] Patient  : {patient_msg[:90]}{'...' if len(patient_msg) > 90 else ''}")
                try:
                    therapist_msg = therapy_chat(patient_msg, patient_id=patient_id, session_id=SESSION_ID)
                    print(f"         Therapist: {therapist_msg[:90]}{'...' if len(therapist_msg) > 90 else ''}")
                except Exception as e:
                    print(f"  [ERROR] therapy_chat failed on closing turn: {e}")
                print(f"  [Patient ended session naturally at turn {turn + 1}]")
                break

        print(f"  -> Log: logs/{patient_id}_{SESSION_ID}.json")

    print("\n" + "=" * 60)
    print(f"Done. {total} sessions saved to logs/")
    print("Evaluate with: python evaluation/evaluate.py --variant full --logs_dir logs")


if __name__ == "__main__":
    run_simulation()
